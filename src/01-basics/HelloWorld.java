/**
 * HelloWorld - Your First Java Program
 * 
 * This is a simple program that demonstrates the basic structure of a Java program.
 * It prints "Hello, World!" to the console.
 * 
 * Concepts covered:
 * - Class definition
 * - Main method
 * - System.out.println()
 * - Comments
 * 
 * @author BICT Java Foundation Course
 * @version 1.0
 */
public class HelloWorld {
    /**
     * The main method is the entry point of any Java application.
     * JVM starts executing the program from this method.
     * 
     * @param args Command line arguments (not used in this example)
     */
    public static void main(String[] args) {
        // Print message to console
        System.out.println("Hello, World!");
        System.out.println("Welcome to Java Programming!");
        
        // You can print multiple lines
        System.out.println("This is my first Java program.");
    }
}
